        <h2><a NAME = "astral">AstralTravel</a></h2>
        <p>AstralTravel allows players to leave their body and explore the world in spectator mode. After a short time the player returns to their body.</p>
        <p>Commands you will need ingame:</p>
        <ul>
            <li><span id="gc">/astral</span> - Leave your body!</li>
            <li>/body - Return to your body.</li>
        </ul>
        
        <h2><a NAME = "bottled">BottledExp</a></h2>
        <p>BottledExp allows players to store their hard earned experience levels in bottles for latter use.</p>
        <p>Commands you will need ingame:</p>
        <ul>
            <li><span id="yc">/bottle get</span> - Get the specified number of XP bottles.</li>
            <li><span id="yc">/bottle get max</span> - Get the maximum number of XP bottles.</li>
        </ul>
        
